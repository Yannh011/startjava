public class Calculator{
	public static void main (String [] args){
	int a = 10;
	int b = 5;
	int sum = a + b;
	int sub = a - b;
	int mult = a * b;
	int div = a / b;
	int mod = a % b;
	int square = a * a;
	int result = 0;
		if (sum == result){
			System.out.println (a + b);
		}
		else if (div == result){
			System.out.println (a / b);
		}
		else if (sub == result){
			System.out.println (a - b);
		}
		else if (mult == result){
			System.out.println (a * b);
		}
		else if (mod == result){
			System.out.println (a % b);
		}
		else if (square == result){
			System.out.println (a * a);
		}
	}
}
